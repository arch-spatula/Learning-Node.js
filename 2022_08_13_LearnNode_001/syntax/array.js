// 정보시스템의 핵심은 CRUD이다. 배열도 똑같다. 배열의 CRUD를 파악하면 배열을 대부분을 파악한 것이다.
// array 리터럴이란 아래와 같다.
let arr = ['A', 'B', 'C', 'D'];  // C 생성하는 방법
console.log(arr); // 배열을 터미널, 콘솔에서 읽는법
arr[2] = 3  //U 업데이트 하는 방법
console.log(arr); // 배열을 터미널, 콘솔에서 읽는법
console.log(arr.length);
arr.push('E');  // 뒤에 붙여넣기 업데이트
console.log(arr);